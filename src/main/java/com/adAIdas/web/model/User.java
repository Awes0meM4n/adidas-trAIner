package com.adAIdas.web.model;

public class User {
	String id;
	String name;
	String dateBirth;
	float vO2;
	float weight;
	float deltaN;
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}
	public String getDateBirth() {
		return dateBirth;
	}
	public float getvO2() {
		return vO2;
	}
	public float getWeight() {
		return weight;
	}
	public float getDeltaN() {
		return deltaN;
	}
	
	
}
